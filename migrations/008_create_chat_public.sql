-- Migration: Create chat_messages, chat_reactions, and chat_typing tables
-- For kicker-wide chat with whisper support, reactions, and typing indicators

SET search_path TO public;

-- 1. Create chat_messages table
CREATE TABLE IF NOT EXISTS chat_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    edited_at TIMESTAMPTZ,
    player_id BIGINT NOT NULL REFERENCES player(id) ON DELETE CASCADE,
    kicker_id BIGINT NOT NULL REFERENCES kicker(id) ON DELETE CASCADE,
    content VARCHAR(500) NOT NULL,
    -- For reply feature (one level deep)
    reply_to_id BIGINT REFERENCES chat_messages(id) ON DELETE SET NULL,
    -- For whisper/private messages (NULL = public message)
    recipient_id BIGINT REFERENCES player(id) ON DELETE CASCADE
);

-- Create indexes for chat_messages
CREATE INDEX IF NOT EXISTS idx_chat_messages_kicker_id ON chat_messages(kicker_id);
CREATE INDEX IF NOT EXISTS idx_chat_messages_player_id ON chat_messages(player_id);
CREATE INDEX IF NOT EXISTS idx_chat_messages_created_at ON chat_messages(created_at DESC);
CREATE INDEX IF NOT EXISTS idx_chat_messages_recipient_id ON chat_messages(recipient_id);
CREATE INDEX IF NOT EXISTS idx_chat_messages_reply_to_id ON chat_messages(reply_to_id);

-- 2. Create chat_reactions table
CREATE TABLE IF NOT EXISTS chat_reactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    message_id BIGINT NOT NULL REFERENCES chat_messages(id) ON DELETE CASCADE,
    player_id BIGINT NOT NULL REFERENCES player(id) ON DELETE CASCADE,
    kicker_id BIGINT NOT NULL REFERENCES kicker(id) ON DELETE CASCADE,
    reaction_type TEXT NOT NULL,
    UNIQUE(message_id, player_id, reaction_type)
);

-- Create indexes for chat_reactions
CREATE INDEX IF NOT EXISTS idx_chat_reactions_message_id ON chat_reactions(message_id);
CREATE INDEX IF NOT EXISTS idx_chat_reactions_player_id ON chat_reactions(player_id);

-- 3. Create chat_typing table for typing indicators
CREATE TABLE IF NOT EXISTS chat_typing (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    player_id BIGINT NOT NULL REFERENCES player(id) ON DELETE CASCADE,
    kicker_id BIGINT NOT NULL REFERENCES kicker(id) ON DELETE CASCADE,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(player_id, kicker_id)
);

-- Create indexes for chat_typing
CREATE INDEX IF NOT EXISTS idx_chat_typing_kicker_id ON chat_typing(kicker_id);
CREATE INDEX IF NOT EXISTS idx_chat_typing_updated_at ON chat_typing(updated_at);

-- 4. Enable Row Level Security
ALTER TABLE chat_messages ENABLE ROW LEVEL SECURITY;
ALTER TABLE chat_reactions ENABLE ROW LEVEL SECURITY;
ALTER TABLE chat_typing ENABLE ROW LEVEL SECURITY;

-- 5. RLS Policies for chat_messages

-- Users can view public messages OR whispers where they are sender/recipient
CREATE POLICY "Users can view chat messages"
ON chat_messages FOR SELECT
USING (
    EXISTS (
        SELECT 1 FROM player
        WHERE player.kicker_id = chat_messages.kicker_id
        AND player.user_id = auth.uid()
    )
    AND (
        -- Public message (no recipient)
        chat_messages.recipient_id IS NULL
        OR
        -- User is the sender
        EXISTS (
            SELECT 1 FROM player
            WHERE player.id = chat_messages.player_id
            AND player.user_id = auth.uid()
        )
        OR
        -- User is the recipient
        EXISTS (
            SELECT 1 FROM player
            WHERE player.id = chat_messages.recipient_id
            AND player.user_id = auth.uid()
        )
    )
);

-- Users can insert messages if they have a player in that kicker
CREATE POLICY "Users can insert chat messages"
ON chat_messages FOR INSERT
WITH CHECK (
    EXISTS (
        SELECT 1 FROM player
        WHERE player.kicker_id = chat_messages.kicker_id
        AND player.user_id = auth.uid()
        AND player.id = chat_messages.player_id
    )
);

-- Users can update their own messages
CREATE POLICY "Users can update own chat messages"
ON chat_messages FOR UPDATE
USING (
    EXISTS (
        SELECT 1 FROM player
        WHERE player.id = chat_messages.player_id
        AND player.user_id = auth.uid()
    )
)
WITH CHECK (
    EXISTS (
        SELECT 1 FROM player
        WHERE player.id = chat_messages.player_id
        AND player.user_id = auth.uid()
    )
);

-- Only kicker admin can delete messages
CREATE POLICY "Admins can delete chat messages"
ON chat_messages FOR DELETE
USING (
    EXISTS (
        SELECT 1 FROM kicker
        WHERE kicker.id = chat_messages.kicker_id
        AND kicker.admin = auth.uid()
    )
);

